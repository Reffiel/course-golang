package main

import "fmt"

/*Retorno nomeado deixa a função mais limpa e não é necessário declarar as variáveis*/
func calculosMatematicos(n1, n2 int) (soma int, subtracao int) {
	soma = n1 + n2
	subtracao = n1 - n2
	return
}

func main() {
	soma, subtracao := calculosMatematicos(10, 20)
	fmt.Println(soma, subtracao)
}
